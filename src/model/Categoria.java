package model;

/**
 *
 * @author jordi
 */
public class Categoria {

    private int id;
    private String nom;

    public Categoria() {
    }

    public Categoria(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    @Override
    public String toString() {
        return "Categoria{" + "id=" + id + ", nom=" + nom + '}';
    }

}
